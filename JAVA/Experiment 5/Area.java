import java.util.*;
interface area
{
    	static final double pi=3.142;
    	double compute(double x);
}
class square implements area
{
    	public double compute(double x)
    	{
        	return x*x;
    	}
}
class circle implements area
{
    	public double compute(double x)
    	{
        	return pi*x*x;
    	}
}
public class Experiment5_3
{
    	public static void main(String[] args) 
    	{
        	Scanner Sc=new Scanner(System.in);
        	System.out.println("Enter the side od square and radius of circle:");
        	int a=Sc.nextInt();
        	int r=Sc.nextInt();
        	square s=new square();
        	circle c=new circle();
        	System.out.println("Area of square: " + s.compute(a));
        	System.out.println("Area of circle: " + c.compute(r));
    	}
}
