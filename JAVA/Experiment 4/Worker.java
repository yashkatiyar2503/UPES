import java.util.*;
class worker
{ 
    void display(String name)
    {
        System.out.println("Employee name : "+name);
    }
}
class dailyworker extends worker
{
    void compay(String name, int rate, int h)
    {
        super.display(name);
        System.out.println("Salary : "+h*rate);
    }
}
class salariedworker extends worker
{
    int hour=40;
    void compay(String name, int rate)
    {
        super.display(name);
        System.out.println("Salary : "+rate*hour);
    }
}
public class Example_3
{
    public static void main(String args[])
    {
        String name1, name2;
        int rate1, rate2;
        int h;
        Scanner Sc=new Scanner(System.in);
        System.out.println("Enter the name, rate of wages and number of hourse worked of daily worker:");
        name1=Sc.next();
        rate1=Sc.nextInt();
        h=Sc.nextInt();
        System.out.println("Enter the name and rate of wages of salaried worker:");
        name2=Sc.next();
        rate2=Sc.nextInt();
        dailyworker d=new dailyworker();
        d.compay(name1, rate1, h);
        salariedworker s=new salariedworker();
        s.compay(name2, rate2);
    }
}
